package com.yenaly.circularrevealswitch

/**
 * Enum class representing the types of switch animations.
 *
 * This enum is used to define the two types of animations that can be used
 * when switching between themes in the application.
 *
 * @property SHRINK Represents the animation where the theme switch appears to shrink.
 * @property EXPAND Represents the animation where the theme switch appears to expand.
 */
enum class SwitchAnimation {
    SHRINK,
    EXPAND;
}